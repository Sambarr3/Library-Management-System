    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(txtbid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Book Id is Required!");
            txtbid.requestFocus();
        }
        else if(txtbname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Book Name is Required!");
            txtbname.requestFocus();
        }
        else if(txtbpublisher.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Book Publisher Name is Required!");
            txtbpublisher.requestFocus();
        }
        else if(txtblaunch.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Year of Book Launched is Required!");
            txtblaunch.requestFocus();
        }
        else if(txtbprice.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Book Price is Required!");
            txtbprice.requestFocus();
        }
        else {
            try {
                Connection con=Connect.Connection();
                PreparedStatement pst = con.prepareStatement("INSERT INTO `library`.`book` (`id`,`name`,`publisher`,`price`,`year`,`status`) values(?,?,?,?,?,?)");
                pst.setString(1, txtbid.getText());
                pst.setString(2, txtbname.getText());
                pst.setString(3, txtbpublisher.getText());
                pst.setString(4, txtbprice.getText());
                pst.setString(5, txtblaunch.getText());
                pst.setString(6, "NOT-ISSUED");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Book Record has been Saved.");
                txtbid.setText("");
                txtbname.setText("");
                txtbpublisher.setText("");
                txtbprice.setText("");
                txtblaunch.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

