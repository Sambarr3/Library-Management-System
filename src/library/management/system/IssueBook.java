    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(txtbid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Book Id is Required!");
            txtbid.requestFocus();
        }
        else if(txtstdid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Student ID is Required!");
            txtstdid.requestFocus();
        }
        else if(txtdue.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Due Date of Book is Required!");
            txtdue.requestFocus();
        }
        else { 
            try {
                ps=con.prepareStatement("UPDATE BOOK SET STATUS=?, ISSUEDATE=?, DUEDATE=?, STUDENTID=?");
                ps.setString(1, "ISSUED");
                ps.setString(2, txtissue.getText());
                ps.setString(3, txtdue.getText());
                ps.setString(4, txtstdid.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Book has been Issued.");
                txtbid.setText("");
                txtstdid.setText("");
                txtbname.setText("");
                txtissue.setText("");
                txtdue.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        if(txtbid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Book ID and Search!");
            txtbid.requestFocus();
        }
        else{
            try {
                ps=con.prepareStatement("SELECT * FROM BOOK WHERE ID=?");
                ps.setString(1, txtbid.getText());
                rs=ps.executeQuery();
                if(rs.next()){
                    txtbname.setText(rs.getString(2));
                }
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

