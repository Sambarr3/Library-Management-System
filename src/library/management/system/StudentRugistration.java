    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "StudentId is Required!");
            txtid.requestFocus();
        }
        else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Student Name is Required!");
            txtname.requestFocus();
        }
        else if(txtcourse.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Course Name is Required!");
            txtcourse.requestFocus();
        }
        else if(txtbranch.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Baranch Name is Required!");
            txtbranch.requestFocus();
        }
        else if(txtsemester.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Semester is Required!");
            txtsemester.requestFocus();
        }
        else {
            try {
                Connection con=Connect.Connection();
                PreparedStatement pst=con.prepareStatement("INSERT INTO `library`.`student` (`id`,`name`,`course`,`branch`,`semester`) values(?,?,?,?,?)");
                pst.setString(1, txtid.getText());
                pst.setString(2, txtname.getText());
                pst.setString(3, txtcourse.getText());
                pst.setString(4, txtbranch.getText());
                pst.setString(5, txtsemester.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved.");
                txtid.setText("");
                txtname.setText("");
                txtcourse.setText("");
                txtbranch.setText("");
                txtsemester.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(StudentRugistration.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

