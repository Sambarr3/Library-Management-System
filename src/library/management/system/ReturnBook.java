    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        if(txtstdid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Student Id to Search!");
            txtstdid.requestFocus();
        }
        else{
            try {
                ps = con.prepareStatement("select student.name,book.id,book.name,book.issuedate,book.duedate from book inner join student using(id) where book.id=?");
                ps.setString(1, txtstdid.getText());
                rs=ps.executeQuery();
                if(rs.next()){
                    txtbid.setText(rs.getString(2));
                    txtstdname.setText(rs.getString(1));
                    txtbname.setText(rs.getString(3));
                    txtissue.setText(rs.getString(4));
                    txtdue.setText(rs.getString(5));
                }
                else{
                    JOptionPane.showMessageDialog(this, "Please enter valid Student ID!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

