    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed
        // TODO add your handling code here:
        if (txtstdid.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter Student Id to Search!");
        txtstdid.requestFocus();
    } else {
        try {
            // Update the book record to indicate it has been returned
            ps = con.prepareStatement("UPDATE BOOK SET STATUS=?, ISSUEDATE=NULL, DUEDATE=NULL, STUDENTID=NULL WHERE BOOK.ID=?");
            ps.setString(1, "AVAILABLE");  // Set status to AVAILABLE
            ps.setString(2, txtbid.getText());
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Book Returned.");
            
            // Clear the form fields
            txtissue.setText("");
            txtdue.setText("");
            txtstdid.setText("");
            txtstdname.setText("");
            txtbname.setText("");
            txtbid.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }   
    }//GEN-LAST:event_btnreturnActionPerformed

